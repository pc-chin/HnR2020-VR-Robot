package io.github.unununium.comm;

import android.content.BroadcastReceiver;
import android.content.Context;
import android.content.Intent;
import android.net.ConnectivityManager;
import android.net.NetworkInfo;

import org.jetbrains.annotations.NotNull;

/** A broacast receiver that listens for network changes in the background. **/
public class NetworkStateListener extends BroadcastReceiver {
    private ServerConnection connection;

    // Default public constructor
    public NetworkStateListener() {}

    public NetworkStateListener(ServerConnection connection) {
        this.connection = connection;
    }

    @Override
    public void onReceive(@NotNull Context context, Intent intent) {
        ConnectivityManager conn =  (ConnectivityManager)
                context.getSystemService(Context.CONNECTIVITY_SERVICE);
        NetworkInfo networkInfo = conn.getActiveNetworkInfo();
        connection.onNetworkStateChanged(networkInfo != null && networkInfo.isConnected());
        throw new UnsupportedOperationException("Not yet implemented");
    }
}